# 1.	O que é uma tupla em Python? 
# Resposta: Uma tupla em Python é uma estrutura de dados imutável que 
# contém uma sequência ordenada de elementos.

# 2.	Como criar uma tupla em Python? 
# Resposta: Para criar uma tupla em Python, basta colocar vários elementos 
# separados por vírgulas entre parênteses. Exemplo: minha_tupla = (1, 2, 3).

# 3.	Como acessar elementos em uma tupla em Python? 
# Resposta: Para acessar elementos em uma tupla em Python, você pode usar índices. 
# Exemplo: minha_tupla[0] retorna o primeiro elemento da tupla.

# 4.	Como alterar elementos em uma tupla em Python? 
# Resposta: Não é possível alterar elementos em uma tupla em Python, 
# pois as tuplas são imutáveis.

# 5.	Como adicionar elementos em uma tupla em Python? 
# Resposta: Não é possível adicionar elementos em uma tupla em Python, pois as tuplas são 
# imutáveis. No entanto, você pode criar uma nova tupla combinando duas tuplas existentes.
